    @SuppressLint("DefaultLocale")
    public String tranEngToChi(String eng) {
        if (eng == null) {
            return "";
        }
        final String english = eng.toLowerCase();
        String numberStr = "";
        for (int i = 0; i < english.length(); i++) {
            try {
                int parseInt = Integer.parseInt(english.substring(i, i + 1));
                numberStr = numberStr + parseInt;
            } catch (Exception e) {
            }
        }
        String string = mDatsLengths.get(english);
        if (string == null) {
            String currStr = english;
            String currStr2 = currStr;
            String chinStr = "";
            for (int i = currStr.length() - 1; i > 0; i--) {
                currStr2 = currStr2.substring(0, i);
                String chinStr2 = mDatsShorts.get(currStr2);
                if (chinStr2 != null) {
                    chinStr += chinStr2;
                    currStr = currStr.substring(currStr2.length());
                    currStr2 = currStr;
                    i = currStr.length();
                }
            }
            if (chinStr == null) {
                return english;
            } else {
                return chinStr + numberStr;
            }
        } else {
            return string + numberStr;
        }
    }
